#! /bin/bash

if ! command -v vault 2>&1 >/dev/null
then
    echo "[INSTALLING ⬇️ ] Vault"
    wget --progress=dot:giga -O - https://apt.releases.hashicorp.com/gpg | sudo gpg --dearmor -o /usr/share/keyrings/hashicorp-archive-keyring.gpg
    echo "deb [arch=$(dpkg --print-architecture) signed-by=/usr/share/keyrings/hashicorp-archive-keyring.gpg] https://apt.releases.hashicorp.com $(grep -oP '(?<=UBUNTU_CODENAME=).*' /etc/os-release || lsb_release -cs) main" | sudo tee /etc/apt/sources.list.d/hashicorp.list
    sudo apt update && sudo apt install vault
    echo "- [CHECKED ✅] vault command installed!"
else
    echo "- [CHECKED ✅] vault command exists"
fi